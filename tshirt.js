// Function to get all products in a flat array
function getAllTshirtProducts() {
    return [
        ...alltshirts.rcb,
        ...alltshirts.ferrari,
        ...alltshirts.dc
    ];
}

// Function to get product by ID
function getTshirtProductById(productId) {
    return getAllTshirtProducts().find(product => product.id === parseInt(productId));
}

// Extend the main product functions
const originalGetAllProducts = window.getAllProducts || function() { return []; };
const originalGetProductById = window.getProductById || function() { return null; };

// Override getAllProducts to include tshirt products
window.getAllProducts = function() {
    return [
        ...originalGetAllProducts(),
        ...getAllTshirtProducts()
    ];
};

// Override getProductById to search in both product sets
window.getProductById = function(productId) {
    return originalGetProductById(productId) || getTshirtProductById(productId);
};

// Export the tshirt-specific functions
window.alltshirts = alltshirts;
window.getAllTshirtProducts = getAllTshirtProducts;
window.getTshirtProductById = getTshirtProductById; 